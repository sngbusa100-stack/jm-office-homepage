export function findReplyTemplate(topic: string): ReplyTemplate | undefined {
  return replyTemplates.find((t) => t.topic === topic);
}

/** 템플릿에 이름·접수번호를 채워 회신문 전문을 만든다. */
export function buildReply(template: ReplyTemplate, params: { name: string; id: string }): string {
  const greeting = GREETING.replace('{이름}', params.name).replace('{접수번호}', params.id);
  const questions = template.checkQuestions.map((q, i) => `${i + 1}) ${q}`).join('\n');
  return [greeting, '', template.intro, '', questions, '', CLOSING].join('\n');
}
